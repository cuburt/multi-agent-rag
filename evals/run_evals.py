import time
import httpx
from litellm import completion

# Base URL for local FastAPI app
API_URL = "http://localhost:8000"

def evaluate_correctness(query: str, expected_answer: str, actual_answer: str) -> float:
    """Uses LLM-as-a-judge to evaluate correctness on a scale of 0-1"""
    prompt = f"""
    Given the User Query, Expected Answer, and the Actual Answer generated by an AI assistant:
    Rate the Actual Answer's correctness on a scale of 0 to 1 (e.g., 0.0, 0.5, 1.0).
    Output ONLY the float number.

    User Query: {query}
    Expected Answer: {expected_answer}
    Actual Answer: {actual_answer}
    """
    try:
        res = completion(model="gemini/gemini-1.5-flash", messages=[{"role": "user", "content": prompt}])
        score_str = res.choices[0].message.content.strip()
        return float(score_str)
    except Exception as e:
        print(f"Eval Error: {e}")
        return 0.0

def run_evals():
    dataset = [
        {"query": "What is your cancellation policy?", "tenant_id": "tenant_1", "expected": "24 hours in advance to avoid a $50 fee"},
        {"query": "What is your cancellation policy?", "tenant_id": "tenant_2", "expected": "48 hours notice or $75 fee"},
    ]

    results = []
    
    for item in dataset:
        print(f"Evaluating: {item['query']} (Tenant: {item['tenant_id']})")
        start = time.time()
        try:
            resp = httpx.post(f"{API_URL}/ask", json={
                "query": item["query"],
                "tenant_id": item["tenant_id"]
            }, timeout=10.0)
            data = resp.json()
            latency = time.time() - start
            
            actual_answer = data["answer"]
            citations = data.get("citations", [])
            
            score = evaluate_correctness(item["query"], item["expected"], actual_answer)
            grounded = len(citations) > 0
            
            results.append({
                "query": item["query"],
                "tenant": item["tenant_id"],
                "score": score,
                "latency_s": round(latency, 2),
                "grounded": grounded
            })
        except Exception as e:
            print(f"Failed to call API: {e}")
            
    # Summary
    if results:
        avg_score = sum(r["score"] for r in results) / len(results)
        avg_lat = sum(r["latency_s"] for r in results) / len(results)
        print("\n--- Eval Summary ---")
        print(f"Total Evaluated: {len(results)}")
        print(f"Average Correctness: {avg_score:.2f}")
        print(f"Average Latency: {avg_lat:.2f}s")
        for r in results:
            print(f"- [Score: {r['score']}] [Grounded: {r['grounded']}] {r['query']}")
    else:
        print("No results to summarize. Is the server running?")

if __name__ == "__main__":
    run_evals()
